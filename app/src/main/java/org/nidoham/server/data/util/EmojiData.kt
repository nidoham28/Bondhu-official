// EmojiData.kt
//
// Single source of truth for all emoji content used in the chat emoji picker.
//
// HOW TO ADD MORE EMOJIS
// ─────────────────────
//  • To add to an existing category: append strings to the `emojis` list of
//    the matching EmojiCategory in EMOJI_CATEGORIES below.
//  • To add a brand-new category: append a new EmojiCategory(...) entry to
//    EMOJI_CATEGORIES. The tab bar and grid will pick it up automatically.
//  • To change the default recent seeds: edit DEFAULT_RECENT_EMOJIS.
//
// No other file needs to change.
// ═════════════════════════════════════════════════════════════════════════════

// ─── Data Model ──────────────────────────────────────────────────────────────

