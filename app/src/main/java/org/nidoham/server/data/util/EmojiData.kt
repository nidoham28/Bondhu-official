/**
 * Returns true when the entire (trimmed) string contains only emoji codepoints,
 * variation selectors, ZWJ sequences, skin-tone modifiers, and whitespace.
 * Used to decide whether to render a "big emoji" bubble with no background.
 *
 * Covers all standard emoji ranges including regional indicator flags
 * (U+1F1E0–U+1F1FF) and miscellaneous symbol blocks.
 */
fun String.isOnlyEmoji(): Boolean {
    if (isBlank()) return false
    val cleaned = trim()
        .replace(Regex("[\u200D\uFE0F\u20E3]"), "")        // ZWJ, variation-16, combining enclosing keycap
        .replace(Regex("\uD83C[\uDFFB-\uDFFF]"), "")       // Fitzpatrick skin-tone modifiers (surrogate pair)
    if (cleaned.isEmpty()) return false
    var i = 0
    while (i < cleaned.length) {
        val cp = cleaned.codePointAt(i)
        val isEmoji = cp in 0x1F300..0x1FAFF  // Misc symbols, pictographs, transport, supplemental
                || cp in 0x1F1E0..0x1F1FF     // Regional indicators (flag sequences)
                || cp in 0x2600..0x27BF        // Misc symbols & dingbats
                || cp in 0xFE00..0xFE0F        // Variation selectors
                || cp == 0x200D                // Zero-width joiner
                || Character.isWhitespace(cp)
                || Character.getType(cp) == Character.OTHER_SYMBOL.toInt()
        if (!isEmoji) return false
        i += Character.charCount(cp)
    }
    return true
}

/**
 * Counts visible emoji units in the string (after stripping modifiers).
 * Used to pick an appropriate font size for big-emoji bubbles.
 */
fun String.emojiCount(): Int {
    val cleaned = trim()
        .replace(Regex("[\u200D\uFE0F\u20E3]"), "")
        .replace(Regex("\uD83C[\uDFFB-\uDFFF]"), "")
        .replace(Regex("\\s"), "")
    var count = 0
    var i = 0
    while (i < cleaned.length) {
        count++
        i += Character.charCount(cleaned.codePointAt(i))
    }
    return count
}